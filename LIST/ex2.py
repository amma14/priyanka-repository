# POSITIVE INDEXING
# positive indexing means it will start with zero("0")
x=[1,2,3,4,5,6,7,8,9]
print(x)
print(type(x))
print(x[1])
print(x[6])
print(x[4])
print(x[7])
# NEGATIVE INDEXING
# negative indexing means it will start with negative values ("-1")
y=['priya','shiva','bgm','vamsi','amma','dady','bujji']
print(y)
print(type(y))
print(y[-7])
print(y[-5])
print(y[-4])
print(y[-3])
print(y[-2])
# SLICING
# slicing means combination of starting value and ending value
# syntax:starting value:ending value
z=[11,22,33,44,55,66,77,88,99]
print(z[3:5])
print(z[0:3])
print(z[3:5])
print(z[6:4])
print(z[6:2])
print(z[1:5])
print(z[0:8])
print(z[4:3*2])
print(z[4:5+2])